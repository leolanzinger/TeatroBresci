<h2>Home</h2>

<h3>Banner in home page (Tipo quello di Antiche mura)</h3>

<p>Per attivare un banner in home page premi su <strong>Festival banner</strong> sulla barra di amministrazione sulla sinistra. Apri "Antiche Mura teatro festival" e scrivi quello che preferisci. Il titolo sarà il titolo e nel testo puoi usare link per rimandare le persone dove preferisci! L'immagine di copertina (in basso a sinistra) può essere modificata e anche quella sarà in homepage.</p>

<p>Per rimuovere il banner é sufficiente modificare lo stato (sulla sinistra). Invece di <strong>pubblicato</strong> premendo su <strong>modifica</strong> é possibile metterlo come <strong>bozza</strong> e in questo modo diventerà invisibile. Forte no?</p>

<h2>Chi siamo</h2>

<h3>Biografia</h3>

<p>Vai su <strong>pagine</strong> dal pannello di amministrazione sulla sinistra. Clicca poi su <strong>Chi siamo</strong>. Da lì trovi il testo modificabile di quella sezione. Non dimenticare di salvare prima di uscire!</p>

<h3>Persone della compagnia</h3>

<p>Vai su <strong>persone</strong> dal pannello di amministrazione sulla sinistra. Per modificare una persona già esistente premi sulla persona. Per crearne una nuova premi su <strong>Aggiungi nuovo</strong>.</p>

<p>Il titolo sarà il nome della persona, la descrizione sarà la sua biografia e l'immagine in evidenza (sulla destra) sarà l'immagine relativa alla persona. Nel dubbio dai un'occhiata a una persona già presente!</p>

<p>Sono preferibili foto rettangolari in orizzontale.</p>

<h2>Calendario</h2>

<p>Le date del calendario si aggiornano sulla pagina calendario e in homepage automagicamente™.</p>

<p>Per aggiungere una data premi su <strong>Eventi</strong> dal pannello di amministrazione sulla sinistra. Da lí premi su <strong>Aggiungi evento</strong>.<br/>
Il <strong>titolo</strong> é il nome dello spettacolo. La <strong>descrizione</strong> non é necessaria. Sulla destra nella casella <strong>Quando</strong> puoi inserire i dati relativi a giorno e ora. Scendendo trovi sempre sulla destra <strong>Categorie eventi</strong>: clicca su spettacolo didattico se si tratta di uno spettacolo didattico. Infine inserisci il luogo in fondo su <strong>dove</strong>.<br/>
Una volta inseriti questi dati premi il tasto azzurro sulla destra "<strong>Pubblica</strong>".</p>

<h2>Spettacoli</h2>

<p>Per aggiungere o rimuovere spettacoli premi su <strong>Spettacoli</strong> dal pannello di amministrazione sulla sinistra.</p>

<p>Clicca sullo spettacolo che vuoi modificare per modificarlo. Clicca su <strong>Cestina</strong> per eliminarlo.</p>

<p>Per creare un nuovo evento premi su <strong>aggiungi nuovo</strong>. Il <strong>Titolo</strong> é il titolo dello spettacolo, la descrizione é poi la descrizione che viene mostrata.<br/>
Per creare una galleria premi su <strong>aggiungi media</strong> e poi, sulla sinistra, <strong>crea galleria</strong>. Una volta creata premi su <strong>inserisci articolo</strong>.</p>

<p>Quando sei soddisfatto del risultato non dimenticare di dare una <strong>categoria</strong> allo spettacolo (sulla destra) e di impostare un'<strong>immagine in evidenza</strong> (in fondo a destra). Ti consiglio di metterne una rettangolare orizzontale per un risultato ottimale.<br/>
Finito tutto? Premi sul tasto azzurro <strong>Pubblica</strong> in alto a destra.</p>

<h2>Corsi</h2>

<p>Per aggiungere o rimuovere corsi premi su <strong>Corsi</strong> dal pannello di amministrazione sulla sinistra.</p>

<p>La creazione, modifica e eliminazione é uguale a quella degli spettacoli.</p>

<h2>Gallery</h2>

<p>La gallery si aggiorna automagicamente™ da Flickr e da Instagram e da Youtube. Quando vengono aggiunti contenuti su questi mezzi verrano mostrati automaticamente sulla pagina Gallery.</p>

<h2>Contatti</h2>

<p>Per modificare i contatti entra su <strong>Pagine</strong> dal pannello di amministrazione e vai su <strong>Contatti</strong>.</p>

<p>In fondo alla pagina puoi trovare i <strong>Campi personalizzati</strong>. Modificali a piacimento, si modificheranno anche sulla pagina dei contatti. Ricorda di premere prima di uscire il tasto <strong>Aggiorna</strong> azzurro in alto a destra. <br/>
<em>Non modificare il nome (tipo codiceFiscale, emailDirezione) ma solo il valore!</em></p>

